//Q: 什么是接口？
//A: 接口(Interface)在语法/语言结构上很像类，但是完全不同。接口中没有方法，没有逻辑，只有成员名称，这些成员名称就是这个接口的定义。换句话说，接口是一个
  //语义合同，是类与类之间的中间人，这个合同定义了接口"是什么"。

//Q: 如何声明一个接口？
//A: 声明一个接口遵循“访问修饰符+关键词Interfac+名称+代码块”的结构。注意接口的名称必须以I开头，标明这是一个接口。在代码块中装有接口的成员，注意所有成员
  //都没有访问修饰符，访问修饰符默认为public，成员可以是属性、字段或方法。
  
//Q: 为什么要使用接口？
//A: 最关键的原因，是跟访问修饰符、类的组合关系一样，为了让整个程序符合松耦合设计。

