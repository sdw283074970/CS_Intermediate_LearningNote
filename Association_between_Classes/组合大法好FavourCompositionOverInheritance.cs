  //1.继承由于其拥有强大方便的功能，及其容易被年轻的程序员滥用，导致生成一个庞大的、错综复杂的类的层级关系，让整个结构变得脆弱；
  //2.紧耦合。庞大的层级关系即使奋力维护，当项目必须根据需求产生变动的时候，牵一发动全身，所有相关联的层级或都将重新部署，工作量庞大得甚至能废掉项目。

//总结：
  //继承的优点：代码复用，易于理解；
  //继承的缺点：紧耦合，脆弱，易于被滥用。
  //继承绝对不是一个坏的东西，它很强大，也很危险。只要使用得当，与组合相结合，一样能为程序的运行效率做出贡献。
  
